def scan_forbidden_terms() -> list[dict[str, str]]:
    violations = []
    for path in SCAN_FILES:
        if not path.exists():
            continue
        text = path.read_text(encoding="utf-8", errors="ignore")
        for term in FORBIDDEN_TERMS:
            if term in text:
                violations.append({"path": str(path.relative_to(ROOT)), "term": term})
    return violations


