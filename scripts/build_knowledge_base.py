def make_cards(domain: str, labels: Iterable[str], what_template: str, *, kind: str = "common_knowledge") -> list[dict]:
    cards = []
    for raw_label in labels:
        aliases = alias_parts(raw_label)
        label = clean_label(raw_label)
        answers = {
            "what": what_template.format(label=label),
            "how": style_line(label, domain),
        }
        if label in SPECIFIC_FACTS:
            answers.update(SPECIFIC_FACTS[label])
        cards.append(
            {
                "label": label,
                "aliases": aliases,
                "kind": kind,
                "domain": domain,
                "answers": answers,
            }
        )
    return cards


def make_cross_cards(
    domain: str,
    lefts: Iterable[str],
    rights: Iterable[str],
    what_template: str,
    *,
    label_template: str = "{left}{right}",
    how_template: str | None = None,
    kind: str = "common_knowledge",
) -> list[dict]:
    cards = []
    for raw_left in lefts:
        left = clean_label(raw_left)
        for raw_right in rights:
            right = clean_label(raw_right)
            label = label_template.format(left=left, right=right)
            answers = {
                "what": what_template.format(left=left, right=right, label=label),
            }
            if how_template:
                answers["how"] = how_template.format(left=left, right=right, label=label)
            if label in SPECIFIC_FACTS:
                answers.update(SPECIFIC_FACTS[label])
            cards.append(
                {
                    "label": label,
                    "aliases": [label],
                    "kind": kind,
                    "domain": domain,
                    "answers": answers,
                }
            )
    return cards


